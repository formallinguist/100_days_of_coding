#For loop

x = ['navin',65,2.5] # x is a list

for i in x:
    print(i)

y = 'apple' #y is a string.

for i in y:
    print(i)

z = [2,6,'paul']

for i in z:
    print(i)


#using range function.

for i in range(10):
    print(i)

#using range function in reverse order.

for i in range(20,11,-1):
    print(i)

# using if in a for loop

for i in range (20,11,-1):
    if i % 5 != 0:
        print(i)



